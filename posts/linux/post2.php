		<h2>Installation of Haskell Compiler</h2>
		<p class="texto">The Haskell compiler is <strong>GHC</strong> which means <strong>Glasgow Haskell Compiler</strong>. The installation of GHC brings <strong>GHCI</strong> alongside with it, which is the interpreter where you can test functions you've been developing. To install Haskell compiler, digit the next command on <strong>ubuntu</strong>'s terminal.</p>
		<div class="code-box">
			<div class="code">
				<span style="color:green">linux@pc:~$</span>
				<span style="color:red">sudo apt-get install haskell-platform</span>
			</div>
		</div>
		<p class="texto">For others linux distributions check on official <a class="hyper-link" href="https://www.haskell.org/platform/linux.html"><strong>Haskell Website</strong></a>.</p>
